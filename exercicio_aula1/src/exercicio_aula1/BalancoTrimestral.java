package exercicio_aula1;

public class BalancoTrimestral {

	public static void main(String[] args) {
		int gastosJaneiro = 15000;
		int gastosFevereiro = 23000;
		int gastosMarço = 17000;
		int gastoTrimestral = 0;
		gastoTrimestral = gastosJaneiro + gastosFevereiro + gastosMarço;
	
	System.out.println("O gasto trimestral é: " + gastoTrimestral);	
		
	}
}
